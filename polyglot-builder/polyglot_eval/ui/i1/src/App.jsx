import { useState, useEffect, useRef } from 'react'
import { erData } from './data.js'

const TABS = ['Diagram', 'Entities', 'Relationships']

export default function App() {
  const [activeTab, setActiveTab] = useState('Diagram')
  const [selectedEntity, setSelectedEntity] = useState(null)
  const [mermaidReady, setMermaidReady] = useState(false)
  const [copied, setCopied] = useState(false)
  const [showRaw, setShowRaw] = useState(false)
  const diagramRef = useRef(null)

  useEffect(() => {
    const script = document.createElement('script')
    script.type = 'module'
    script.textContent = `
      import mermaid from 'https://cdn.jsdelivr.net/npm/mermaid@10/dist/mermaid.esm.min.mjs';
      window.__mermaid = mermaid;
      mermaid.initialize({
        startOnLoad: false,
        theme: 'dark',
        er: { diagramPadding: 40, layoutDirection: 'TB', minEntityWidth: 100, minEntityHeight: 75, entityPadding: 15, useMaxWidth: false },
        themeVariables: {
          primaryColor: '#1e1a3a',
          primaryTextColor: '#e8e4ff',
          primaryBorderColor: '#7c6aff',
          lineColor: '#5a4dcc',
          secondaryColor: '#12103a',
          tertiaryColor: '#0a0820',
          background: '#07071a',
          mainBkg: '#1e1a3a',
          nodeBorder: '#7c6aff',
          clusterBkg: '#0a0820',
          titleColor: '#f1f1f6',
          edgeLabelBackground: '#12103a',
          attributeBackgroundColorEven: '#161530',
          attributeBackgroundColorOdd: '#1e1a3a',
          fontFamily: 'Inter, system-ui, sans-serif',
          fontSize: '13px'
        }
      });
      window.dispatchEvent(new Event('mermaid-ready'));
    `
    document.head.appendChild(script)
    const handler = () => setMermaidReady(true)
    window.addEventListener('mermaid-ready', handler)
    return () => window.removeEventListener('mermaid-ready', handler)
  }, [])

  useEffect(() => {
    if (!mermaidReady || activeTab !== 'Diagram' || !diagramRef.current) return
    const render = async () => {
      try {
        diagramRef.current.innerHTML = ''
        const { svg } = await window.__mermaid.render('er-diagram', erData.mermaidDiagram)
        diagramRef.current.innerHTML = svg
        const svgEl = diagramRef.current.querySelector('svg')
        if (svgEl) {
          svgEl.style.maxWidth = 'none'
          svgEl.removeAttribute('width')
          const h = svgEl.getAttribute('height')
          if (h && parseInt(h) < 300) svgEl.setAttribute('height', '600')
        }
      } catch (e) {
        diagramRef.current.innerHTML = `<pre style="color:#f87171;font-family:monospace;font-size:0.8rem;white-space:pre-wrap;">Mermaid render error: ${e.message}</pre>`
      }
    }
    render()
  }, [mermaidReady, activeTab])

  const copyDiagram = () => {
    navigator.clipboard.writeText(erData.mermaidDiagram)
    setCopied(true)
    setTimeout(() => setCopied(false), 2000)
  }

  const uniqueSources = [...new Set(erData.entities.map(e => e.sourceFile))]
  const explicitRels = erData.relationships.filter(r => r.type === 'explicit').length
  const inferredRels = erData.relationships.filter(r => r.type === 'inferred').length

  return (
    <div className="app">
      {/* Sidebar */}
      <aside className="sidebar">
        <div className="sidebar-header">
          <div className="sidebar-brand">
            <div class="sidebar-logo">🗄️</div>
            <div>
              <div className="sidebar-title">ER Diagram</div>
              <div className="sidebar-sub">polyglot-eval · I1</div>
            </div>
          </div>
          <div className="sidebar-counts">
            <span className="sidebar-count c-purple">{erData.entities.length} entities</span>
            <span className="sidebar-count c-teal">{erData.relationships.length} relations</span>
          </div>
        </div>

        <div className="sidebar-section-label">Entities</div>
        <div className="entity-list">
          {erData.entities.map(e => {
            const hasPK = e.columns.some(c => c.isPK)
            return (
              <div
                key={e.name}
                className={`entity-item ${selectedEntity === e.name ? 'selected' : ''}`}
                onClick={() => { setSelectedEntity(e.name); setActiveTab('Entities') }}
              >
                <span className="entity-dot" />
                {e.name}
                {hasPK && <span className="entity-pk">PK</span>}
              </div>
            )
          })}
        </div>
      </aside>

      {/* Main */}
      <main className="main">
        <header className="header">
          <div>
            <h1>ER Diagram — {erData.repoName}</h1>
            <p className="subtitle">Generated by polyglot-eval I1 · {new Date(erData.generatedAt).toLocaleString()}</p>
          </div>
          <div className="badges">
            <span className="badge badge-purple">{erData.entities.length} Entities</span>
            <span className="badge badge-teal">{erData.relationships.length} Relationships</span>
            <span className="badge badge-blue">{uniqueSources.length} Source Files</span>
          </div>
        </header>

        <div className="tab-bar">
          {TABS.map(t => (
            <button key={t} className={`tab ${activeTab === t ? 'active' : ''}`} onClick={() => setActiveTab(t)}>
              {t}
            </button>
          ))}
        </div>

        <div className="content">
          {activeTab === 'Diagram' && (
            <div className="diagram-tab">
              <div className="diagram-container" ref={diagramRef}>
                {!mermaidReady && <p className="loading">Loading Mermaid diagram engine…</p>}
              </div>
              <div className="diagram-toolbar">
                <div className="diagram-actions">
                  <button className="btn-tool" onClick={() => setShowRaw(!showRaw)}>
                    {showRaw ? '▲ Hide' : '▼ Show'} Source
                  </button>
                  <button className="btn-tool" onClick={copyDiagram}>
                    {copied ? '✓ Copied' : '📋 Copy Mermaid'}
                  </button>
                </div>
                <span className="diagram-hint">Scroll to explore · {erData.entityCount} entities · {erData.relationshipCount} relationships</span>
              </div>
              {showRaw && <pre className="raw-source">{erData.mermaidDiagram}</pre>}
            </div>
          )}

          {activeTab === 'Entities' && (
            <div className="entities-grid">
              {erData.entities.map(entity => (
                <div
                  key={entity.name}
                  className={`entity-card ${selectedEntity === entity.name ? 'highlighted' : ''}`}
                  id={`entity-${entity.name}`}
                >
                  <div className="entity-card-header">
                    <h3>
                      🔷 {entity.name}
                    </h3>
                    <span className="source-badge">{entity.sourceFile.split('/').pop()}:{entity.sourceLine}</span>
                  </div>
                  <table className="column-table">
                    <thead>
                      <tr><th>Column</th><th>Type</th><th>Keys</th><th>References</th></tr>
                    </thead>
                    <tbody>
                      {entity.columns.map(col => (
                        <tr key={col.name}>
                          <td className="col-name">{col.name}</td>
                          <td className="col-type">{col.type}</td>
                          <td>
                            {col.isPK && <span className="key-badge pk">PK</span>}
                            {col.isFK && <span className="key-badge fk">FK</span>}
                          </td>
                          <td className="col-ref">{col.references || '—'}</td>
                        </tr>
                      ))}
                    </tbody>
                  </table>
                </div>
              ))}
            </div>
          )}

          {activeTab === 'Relationships' && (
            <div className="relationships-tab">
              <div className="rel-stats">
                <div className="rel-stat"><strong>{erData.relationships.length}</strong> total relationships</div>
                <div className="rel-stat"><strong style={{color:'var(--accent)'}}>{explicitRels}</strong> explicit</div>
                <div className="rel-stat"><strong style={{color:'var(--amber)'}}>{inferredRels}</strong> inferred</div>
              </div>
              {erData.relationships.length === 0 ? (
                <div className="empty-state">No relationships found.</div>
              ) : (
                <table className="rel-table">
                  <thead>
                    <tr><th>From</th><th></th><th>To</th><th>Type</th><th>Label</th><th>Source</th></tr>
                  </thead>
                  <tbody>
                    {erData.relationships.map((r, i) => (
                      <tr key={i} className={r.type === 'inferred' ? 'inferred' : ''}>
                        <td className="rel-entity">{r.from}</td>
                        <td className="rel-arrow">→</td>
                        <td className="rel-entity">{r.to}</td>
                        <td><span className={`type-badge ${r.type}`}>{r.type}</span></td>
                        <td style={{color:'var(--muted)',fontSize:'0.82rem'}}>{r.label}</td>
                        <td className="source-cite">{r.sourceFile.split('/').pop()}:{r.sourceLine}</td>
                      </tr>
                    ))}
                  </tbody>
                </table>
              )}
            </div>
          )}
        </div>
      </main>
    </div>
  )
}
